public class SingleShow {
    private String name;
    private int imageId;

    public SingleShow(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}
