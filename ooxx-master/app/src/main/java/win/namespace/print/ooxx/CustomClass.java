public class CustomClass {
    private String name;
    private int imageId;

    public CustomClass(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }
}
